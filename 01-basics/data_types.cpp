#include <iostream>
using namespace std;

int main() {
    
    int a = 10;
    float b = 5.5;
    char c = 'X';
    double d = 3.14159;
    bool flag = true;

    cout << "Integer: " << a << endl;
    cout << "Float: " << b << endl;
    cout << "Char: " << c << endl;
    cout << "Double: " << d << endl;
    cout << "Bool: " << flag << endl;

    return 0;
}